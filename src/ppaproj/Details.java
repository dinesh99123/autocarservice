    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        df = (DefaultTableModel) jTable3.getModel();
        int selected = jTable3.getSelectedRow();

        int id = Integer.parseInt(df.getValueAt(selected, 0).toString());

        String sparepart = txtsparepart.getText();
        String description = txtdescription.getText();
        String barcode = txtbarcode.getText();
        String qty = txtqty.getText();
        String cost_price = txtcost_price.getText();
        String rental_price = txtrental_price.getText();
        String reorder_level = txtreorder_level.getText();

        try {
            // TODO add your handling code here:
            pst = con.prepareStatement("update spareparts set Spare_Part=?, Description=?, Barcode=?, Qty=?, Cost_Price=?, Rental_Price=?, Reorder_Level=? where id=?");
            pst.setString(1, sparepart);
            pst.setString(2, description);
            pst.setString(3, barcode);
            pst.setString(4, qty);
            pst.setString(5, cost_price);
            pst.setString(6, rental_price);
            pst.setString(7, reorder_level);
            pst.setInt(8, id);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Spare Part Updated");

            txtsparepart.setText("");
            txtdescription.setText("");
            txtbarcode.setText("");
            txtqty.setText("");
            txtcost_price.setText("");
            txtrental_price.setText("");
            txtreorder_level.setText("");
            txtsparepart.requestFocus();
            load();
            jButton1.setEnabled(true);

        } catch (SQLException ex) {
            Logger.getLogger(Details.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

