    public void load() {
        int a;
        try {
            pst = con.prepareStatement("select * from spareparts");
            ResultSet rs = pst.executeQuery();

            ResultSetMetaData rd = rs.getMetaData();
            a = rd.getColumnCount();
            df = (DefaultTableModel) jTable3.getModel();
            df.setRowCount(0);

            while (rs.next()) {
                Vector v2 = new Vector();
                for (int i = 1; i <= a; i++) {
                    v2.add(rs.getInt("id"));
                    v2.add(rs.getString("Spare_Part"));
                    v2.add(rs.getString("Description"));
                    v2.add(rs.getString("Barcode"));
                    v2.add(rs.getString("Qty"));
                    v2.add(rs.getString("Cost_Price"));
                    v2.add(rs.getString("Rental_Price"));
                    v2.add(rs.getString("Reorder_Level"));

                }
                df.addRow(v2);

            }

        } catch (SQLException ex) {
            Logger.getLogger(Details.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

