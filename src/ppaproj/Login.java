        // TODO add your handling code here:
        try{
            String sql = "SELECT * FROM `addreg` WHERE `UserName`=? and `Password`=?";
            
            con = DriverManager.getConnection("jdbc:mysql://localhost/autocarservice","root","");
            pst = con.prepareStatement(sql);
            pst.setString(1, txtName.getText());
            pst.setString(2, tfPassword.getText());
            rs=pst.executeQuery();
            
            if(rs.next())
            {
                Admin ad2= new Admin();
                ad2.setVisible(true);
                this.dispose();
                JOptionPane.showMessageDialog(this, "Login Succesfully!!");
                txtName.setText("");
                tfPassword.setText("");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Username / Password does not match");
            }
            
        }
        catch(Exception e)
        {}
        
        


    

        
        
